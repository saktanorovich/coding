using System;

    class ModularInequality
    {
        public int countSolutions(int[] a, int p)
        {
            Array.Sort(a);
            int n = a.Length, result = 0;
            int prev = -2000000000;
            for (int i = 0; i < n; ++i)
            {
                int x = a[i];
                if (x == prev)
                {
                    continue;
                }
                prev = x;
                long sum = 0;
                for (int j = 0; j < n; ++j)
                {
                    sum += Math.Abs(a[j] - x);
                }
                result += (sum <= p ? 1 : 0);
            }
            long[] suma = new long[n];
            suma[0] = a[0];
            for (int i = 1; i < n; ++i)
            {
                suma[i] = a[i] + suma[i - 1];
            }
            long l, r;
            for (int i = 0; i <= n; ++ i)
            {
                l = 1;
                r = 0;
                if (i == 0)
                {
                    l = (suma[n - 1] - p) / n;
                    if ((suma[n - 1] - p) % n != 0)
                    {
                        l += (suma[n - 1] - p > 0 ? 1 : 0);
                    }
                    r = a[0] - 1;
                }
                else if (i == n)
                {
                    r = (p + suma[n - 1]) / n;
                    if ((p + suma[n - 1]) % n != 0)
                    {
                        r -= (p + suma[n - 1] < 0 ? 1 : 0);
                    }
                    l = a[n - 1] + 1;
                }
                else if (a[i - 1] + 1 <= a[i] - 1)
                {
                    long sum = p - (suma[n - 1] - 2 * suma[i - 1]);
                    int k = 2 * i - n;
                    if (k < 0)
                    {
                        k = -k;
                        sum = -sum;
                        long d = sum / k;
                        if (sum % k != 0)
                        {
                            d += (sum > 0 ? 1 : 0);
                        }
                        l = Math.Max(a[i - 1] + 1, d);
                        r = a[i] - 1;
                    }
                    else if (k > 0)
                    {
                        long d = sum / k;
                        if (sum % k != 0)
                        {
                            d -= (sum < 0 ? 1 : 0);
                        }
                        l = a[i - 1] + 1;
                        r = Math.Min(a[i] - 1, d);
                    }
                    else
                    {
                        if (suma[n - 1] - 2 * suma[i - 1] <= p)
                        {
                            l = a[i - 1] + 1;
                            r = a[i] - 1;
                        }
                    }
                }
                result += (int)(l <= r ? r - l + 1 : 0);
            }
            return result;
        }
